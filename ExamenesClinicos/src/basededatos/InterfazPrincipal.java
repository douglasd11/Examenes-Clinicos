    public Producto buscarProducto(int i){
        Producto producto = new Producto();
        
        Conexion conB = new Conexion();
        Statement st;
        ResultSet rs;
        
        try {
            st=conB.con.createStatement();
            rs=st.executeQuery("select * from producto");
            jTextArea1.setText("------- Productos -------\n");
            
            while (rs.next()) {
                if(rs.getString("nombre").equals(jComboBox1.getItemAt(i))){
                    producto.setNombre(rs.getString("nombre"));
                    producto.setId(rs.getInt("id"));
                    producto.setLote(rs.getString("lote"));
                    break;
                }
            }
            conB.con.close();
        } catch (SQLException e) {
        }
        return producto;
    }
    
