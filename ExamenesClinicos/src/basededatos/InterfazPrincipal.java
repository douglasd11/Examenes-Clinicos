    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        Conexion conB = new Conexion();
        Statement st;
        ResultSet rs;
        
        try {
            st=conB.con.createStatement();
            rs=st.executeQuery("select * from certificado");
            
            jTextArea2.setText("");
            while (rs.next()) {
                jTextArea2.append("id: "+rs.getInt("id")+"  \ncalidad: "+rs.getString("calidad")+
                        "\nanomalias: "+rs.getString("anomalias")+
                        "\nid ensayo: "+rs.getString("idensayo")+"\n\n");
            }
            conB.con.close();
        } catch (SQLException e) {
        }
    }//GEN-LAST:event_jButton5ActionPerformed

