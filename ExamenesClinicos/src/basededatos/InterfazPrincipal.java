    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Ensayo ensayo = new Ensayo();
        
        if(jComboBox1.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null,"Para realizar esta accion primero seleccione un producto");
            return;
        }
        
        try {
            ensayo.setId(Long.parseLong(jTextField1.getText()));
            ensayo.setCodigoJefe(Long.parseLong(jTextField2.getText()));
            ensayo.setMedidas(jTextField3.getText());
            ensayo.setTipoConcreto(jTextField4.getText());
            ensayo.setFecha(jTextField5.getText());
            ensayo.setIdCalidad(Long.parseLong(jTextField6.getText()));
            
            Producto producto = buscarProducto(jComboBox1.getSelectedIndex());
            ensayo.setIdProducto(producto.getId());
            try {
                Conexion conB = new Conexion();
                    
                String query = "INSERT INTO ensayo (id, codigojefe, medidas, tipoconcreto, fecha, idproducto, idcalidad) "
                        + "VALUES('"+ensayo.getId()+"','"+ensayo.getCodigoJefe()+"','"+ensayo.getMedidas()+"','"+
                        ensayo.getTipoConcreto()+"','"+ensayo.getFecha()+"','"+ensayo.getIdProducto()+"','"+ensayo.getIdCalidad()+"')";
                    
                com.mysql.jdbc.Statement stmt = (com.mysql.jdbc.Statement) conB.con.createStatement();
                        
                stmt.executeUpdate(query);
                JOptionPane.showMessageDialog(null,"ensayo registrado con exito");
                conB.close();
                  
                } catch (HeadlessException | SQLException e) {
                    System.out.println(e);
                    JOptionPane.showMessageDialog(null,"error");
                } 
            
            RegistrarControl ventana = new RegistrarControl(laboratorio.getId(), ensayo.getIdCalidad(),ensayo.getId());
            ventana.setVisible(true);
            this.dispose();
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null,"Error ingrese bien los datos");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

